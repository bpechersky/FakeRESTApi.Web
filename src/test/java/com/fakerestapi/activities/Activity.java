package com.fakerestapi.activities;

public class Activity {
    private int id;
    private String title;
    private String dueDate;
    private boolean completed;

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getDueDate() {
        return dueDate;
    }

    public boolean isCompleted() {
        return completed;
    }

}
